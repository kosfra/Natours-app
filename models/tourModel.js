const mongoose = require('mongoose');
const slugify = require('slugify');
// const validator = require('validator');

const tourSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: [true, 'Please add a tour name'],
      unique: true,
      trim: true,
      maxlength: [40, 'A tour name can not be more than 40 characters'],
      minlength: [10, 'A tour name must be at least 10 characters'],
      // validate: [validator.isAlpha, 'Tour name can only contain letters'],
    },
    slug: String,
    duration: {
      type: Number,
      required: [true, 'Please add a tour duration'],
    },
    maxGroupSize: {
      type: Number,
      required: [true, 'A tour must have a group size'],
    },
    difficulty: {
      type: String,
      required: [true, 'A tour must have a difficulty'],
      enum: {
        values: ['easy', 'medium', 'difficult'],
        message: 'Difficulty must be either easy, medium or difficult',
      },
    },
    ratingsAvarage: {
      type: Number,
      default: 4.5,
      min: [1, 'A tour must have at least one rating'],
      max: [5, 'A tour must be below 5.0'],
    },
    ratingsQuantity: {
      type: Number,
      default: 0,
    },
    price: {
      type: Number,
      required: [true, 'Please add a tour price'],
    },
    priceDiscount: {
      type: Number,
      validate: {
        validator: function (value) {
          // Only validate create, not update ( this only points to current document )
          return value < this.price;
        },
        message: 'Discount price ({VALUE}) must be below regular price',
      },
    },
    summary: {
      type: String,
      trim: true,
      required: [true, 'Please add a tour summary'],
    },
    description: {
      type: String,
      trim: true,
    },
    imageCover: {
      type: String,
      required: [true, 'A tour must have an image cover'],
    },
    images: [String],
    createdAt: {
      type: Date,
      default: Date.now(),
      select: false,
    },
    startDates: [Date],
    secretTour: {
      type: Boolean,
      default: false,
    },
  },
  {
    toJSON: { virtuals: true },
    toObject: { virtuals: true },
  },
);

// Virtual properties
tourSchema.virtual('durationWeeks').get(function () {
  return this.duration / 7;
});

/////Middleware Documen: runs before .save() and .create()
tourSchema.pre('save', function (next) {
  this.slug = slugify(this.name, { lower: true });
  next();
});

